package student;


public class StudentInfo {
	String stdName;
	double stdGPA;
	double stdHeight;
	
	public void  setName(String a){
		stdName =a;
	}
	public String getName(){
		return stdName;
	}
	public void setGPA(double a){
		stdGPA =a;
	}
	public double getGPA(){
		return stdGPA;
	}
	public void setHeight(double a){
		stdHeight = a;
	}
	public double getHeight(){
		return stdHeight;
	}
	

}
